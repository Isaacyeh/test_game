export let mapIndex = 0;
export let map = maps[mapIndex];
export const mapStr = "#";

export function isWall(x, y) {
  return map[Math.floor(y)]?.[Math.floor(x)] === mapStr;
}

export function setMap(i) {
  if (i >= 0 && i < maps.length) {
    mapIndex = i;
    map = maps[mapIndex];
  }
}
